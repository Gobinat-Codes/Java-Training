public class Bitwise{
	public static void main(String[] args){
		// bitwise AND
		int a = 5;
		int b = 5;
		int result1 = a & b;
		System.out.println("bitwise AND : " + result1);
		
		// bitwise OR
		int c = 10;
		int d = 2;
		int result2 = c | d;
		System.out.println("bitwise OR : " + result2);
		
		// bitwise XOR
		int e = 7;
		int f = 6;
		int result3 = e ^ f;
		System.out.println("bitwise XOR : " + result3);
	}
}
	