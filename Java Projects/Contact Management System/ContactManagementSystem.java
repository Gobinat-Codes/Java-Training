package org.projects;

class Contact{
	 String name;
	 String phone;
	 
	 public Contact(String name, String phone) {
		 this.name = name;
		 this.phone = phone;
	 }
	 
	 public String getName() {
		 return name;
	 }
	 
	 public String getPhone() {
		 return phone;
	 }
	 
	 public String toString() {
		 return "Contact Name : " + name + " , "+ "Contact Number : " + phone;
	 }
}

